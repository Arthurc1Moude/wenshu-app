const now = Date.now();
const hour = 60 * 60 * 1000;
const day = 24 * hour;

function hoursAgo(n: number): number {
  return now - n * hour;
}

function daysAgoPosts(n: number, hourOfDay = 12): number {
  return now - n * day - (24 - hourOfDay) * hour;
}

function img(seed: string): string {
  return `https://picsum.photos/seed/${seed}/400/400`;
}

function imgs(count: number, baseSeed: string): string[] {
  return Array.from({ length: count }, (_, i) => img(`${baseSeed}_${i + 1}`));
}

